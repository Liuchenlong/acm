#define eps 1e-8
bool zero(double x)
{
    return fabs(x)<=eps;
}
int sign(double x)
{
    if(zero(x))
        return 0;
    if(x>0)
        return 1;
    return -1;
}
struct point
{
    double x,y;
    double tht;
    point(){}
    point(double xx,double yy)
    {
        x=xx;
        y=yy;
    }
    void output()
    {
        printf("(%f,%f)\n",x,y);
    }
    void input()
    {
        scanf("%lf%lf",&x,&y);
    }
    point operator -(const point &b)
    {
        return point(x - b.x, y - b.y);
    }
    point operator +(const point &b)
    {
        return point(x + b.x, y + b.y);
    }
    double operator ^(const point &b)
    {
        return x*b.y - y*b.x;
    }
    double operator *(const point &b)
    {
        return x*b.x + y*b.y;
    }
};

point pa,pb;
int main()
{
    pa.input();
    pb.input();
    int n;
    int cnt=0;
    scanf("%d",&n);
    for(int i=0;i<n;i++)
    {
        long long a,b,c;
        scanf("%I64d%I64d%I64d",&a,&b,&c);
        if(sign(pa.x*a+pa.y*b+c)*sign(pb.x*a+pb.y*b+c)==-1)
            cnt++;
    }
    printf("%d\n",cnt);
    return 0;
}
